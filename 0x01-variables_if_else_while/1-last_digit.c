#include <stdio.h>
/**
 * main - print the last digit of the number stored in the variable n.
 *
 * Description: Using the main function
 * this program prints the last digit of the number stored in the variable n.
 * Return: 0
 */
int main(void)
{
	char ch;
	for (ch = 'a' ; ch <= 'z' ; ch++)
	{
		putchar(ch);
	}
	putchar('\n');
	return (0);
}
